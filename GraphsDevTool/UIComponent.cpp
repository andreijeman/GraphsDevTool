#include "UIComponent.hpp"

////////////////////////////////////////////////////////////////////////////////////////////////////

UIComponent::UIComponent() : lastState(State::Idle)
{
}

UIComponent::UIComponent(const sf::Vector2f& position, const sf::Vector2f& size) :
    UIObject(position, size), lastState(State::Idle)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////

State::ID UIComponent::getState()
{
    return lastState;
}

